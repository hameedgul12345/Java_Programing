public class EscapSequances {
    public static void main(String[] args) {
        //Double Quotes \" \"
        System.out.println("Hello This is \"Double Quotes\"");
        //Single Quotes \' \'
        System.out.println("Hello This is \'Single Quotes\'");
        //Back slash \\ \\
        System.out.println("Hello This is \\Back Slash\\");
        //New Line \n
        System.out.println("New \nLine");
        //Tap \t
        String name="Hameed";
        int age = 25;
        float gpa=3.2f;
        System.out.println("Hi My Name is\t"+name+"I am"+age+"\tyears old"+"GPA is\t"+gpa);

    }
}
